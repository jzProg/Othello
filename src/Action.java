

//represents a 2D point
public class Action {
	
	private int x; // x coordinate for row
	private int y; // y coordinate for column
	
	public Action(int x,int y){
		this.x = x;
		this.y = y;
	}
	
	//returns x coordinate
	public int getX(){
		return x;
	}
	
	//returns y coordinate
	public int getY(){
		return y;
	}
   }
